package it.csi.siac.siaccommonapp.model;

import it.csi.siac.siaccorser.model.file.File;

public class DownloadFileModel extends GenericModel {

	private static final long serialVersionUID = 3600752222895010307L;

	private Integer uid;
	private File file;
	/**
	 * @return the uid
	 */
	public Integer getUid() {
		return uid;
	}
	/**
	 * @param uid the uid to set
	 */
	public void setUid(Integer uid) {
		this.uid = uid;
	}
	/**
	 * @return the file
	 */
	public File getFile() {
		return file;
	}
	/**
	 * @param file the file to set
	 */
	public void setFile(File file) {
		this.file = file;
	}

}
